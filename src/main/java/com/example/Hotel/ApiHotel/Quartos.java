package com.example.Hotel.ApiHotel;

public class Quartos {

    private int numeQuarto;
    private float preco;
    private String suites;
    private String servQuarto;
    private String quantmax;

    public int getNumeQuarto() {
        return numeQuarto;
    }

    public void setNumeQuarto(int numeQuarto) {
        this.numeQuarto = numeQuarto;
    }

    public float getPreco() {
        return preco;
    }

    public void setPreco(float preco) {
        this.preco = preco;
    }

    public String getSuites() {
        return suites;
    }

    public void setSuites(String suites) {
        this.suites = suites;
    }

    public String getServQuarto() {
        return servQuarto;
    }

    public void setServQuarto(String servQuarto) {
        this.servQuarto = servQuarto;
    }

    public String getQuantmax() {
        return quantmax;
    }

    public void setQuantmax(String quantmax) {
        this.quantmax = quantmax;
    }
}
